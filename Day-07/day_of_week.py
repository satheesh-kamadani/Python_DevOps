# Day of the week

day_number = int(input("Enter the number: "))

if day_number == 1:
   print("Monday")
elif day_number == 2:
   print("Tuesday")
elif day_number == 3:
   print("Wednesday")
elif day_number == 4:
   print("Thrusday")
elif day_number == 5:
   print("Friday")
elif day_number == 6:
   print("Satuarday")
elif day_number == 7:
   print("Sunday")
else:
    print("Invalid number. Please enter a number between 1 and 7")

